wanjia::wanjia()
	   :rand(seed()),//读取随机
	   fitX(0,Graphics::max.x-face.x),
	   fitY(0, Graphics::max.y - face.y)
{
	pos.x = fitX(rand);//玩家X位置随机
	pos.y = fitY(rand);//玩家Y位置随机
	static int i = 0;//静态里循环，每次创建一个，i增加一个
	id = i;
	i++;
}

void wanjia::jiance_biankuang()//检测玩家是否超出边界
{
	pos.clamp(Graphics::min, Graphics::max - wanjia::face);
}

void wanjia::movePlayA(const Keyboard& kbd)//玩家A移动
{
	if (kbd.KeyIsPressed(VK_UP))//按上往上移动
		pos.y -= v.y;
	if (kbd.KeyIsPressed(VK_DOWN))//按下往下移动
		pos.y += v.y;
	if (kbd.KeyIsPressed(VK_LEFT))//按左往左移动
		pos.x -= v.x;
	if (kbd.KeyIsPressed(VK_RIGHT))//按右往右移动
		pos.x += v.x;
}

void wanjia::movePlayB(const Keyboard & kbd)//控制玩家B移动
{
	if (kbd.KeyIsPressed('W'))
		pos.y -= v.y;
	if (kbd.KeyIsPressed('S'))
		pos.y += v.y;
	if (kbd.KeyIsPressed('A'))
		pos.x -= v.x;
	if (kbd.KeyIsPressed('D'))
		pos.x += v.x;
}

void wanjia::jifen(const bianbian & bianbian)//如果bianbian被吃ID与，自己玩家ID相同，则加一分
{
	if (bianbian.getchiID() == id)
		score++;
}

